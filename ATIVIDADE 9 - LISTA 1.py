#MEDIDAS DA PISCINA INFO
larg = int(input("Escreva a largura da piscina : "))
comp = int(input("Escreva o comprimento da piscina: " ))
prof = int(input("Escreva a profundidade da piscina: "))

#CALCULAR O VOLUME
volume = larg * comp * prof

print("O volume de água da piscina é:", volume, "metros cúbicos.")